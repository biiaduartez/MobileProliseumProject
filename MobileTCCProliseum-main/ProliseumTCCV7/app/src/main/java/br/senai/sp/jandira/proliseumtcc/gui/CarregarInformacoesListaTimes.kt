    LaunchedEffect(Unit) {
        // Espera por 3 segundos antes de continuar
        delay(5000)
        loading = false
    }


    Box(
        modifier = Modifier
            .fillMaxSize()
            .background(
                brush = Brush.horizontalGradient(
                    listOf(
